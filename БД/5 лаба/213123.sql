Select AUDITORIUM,AUDITORIUM_TYPE,AUDITORIUM_CAPACITY
From AUDITORIUM a
where AUDITORIUM =(Select  AUDITORIUM FROM AUDITORIUM aa
WHERE aa.AUDITORIUM_TYPE=a.AUDITORIUM_TYPE ORDER BY AUDITORIUM_CAPACITY desc)

