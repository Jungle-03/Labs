Select PULPIT.PULPIT_NAME,FACULTY.FACULTY_NAME
From PULPIT,FACULTY
where PULPIT.FACULTY=FACULTY.FACULTY
and FACULTY.FACULTY IN (SELECT PROFESSION.FACULTY
FROM PROFESSION
where(PROFESSION.PROFESSION_NAME Like'%технолог%'))

--задание номер 2
Select PULPIT.PULPIT_NAME,FACULTY.FACULTY_NAME
From PULPIT Inner JOIN FACULTY
ON PULPIT.FACULTY=FACULTY.FACULTY
Where FACULTY.FACULTY IN (SELECT PROFESSION.FACULTY
FROM PROFESSION
where(PROFESSION.PROFESSION_NAME Like'%технолог%'))

--задание номер 3
Select PULPIT.PULPIT_NAME,FACULTY.FACULTY_NAME,PROFESSION.PROFESSION_NAME
From PULPIT Inner JOIN FACULTY
ON PULPIT.FACULTY=FACULTY.FACULTY Inner JOIN PROFESSION
ON FACULTY.FACULTY = PROFESSION.FACULTY
where(PROFESSION.PROFESSION_NAME Like'%технолог%')

